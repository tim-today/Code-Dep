let _lang = localStorage.getItem('lang') || (navigator.language.startsWith('zh') ? 'zh' : 'en');

function t(key) {
  const entry = I18N[key];
  if (!entry) return key;
  return entry[_lang] || entry['en'] || key;
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem('lang', lang);
  render();
}

function getLang() { return _lang; }

// Translate static HTML elements with data-i18n attribute
function translatePage() {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    const entry = I18N[key];
    if (entry) el.textContent = entry[_lang] || entry['en'] || el.textContent;
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    const entry = I18N[key];
    if (entry) el.placeholder = entry[_lang] || entry['en'] || el.placeholder;
  });
  const langBtn = document.getElementById('langBtn');
  if (langBtn) langBtn.textContent = _lang === 'zh' ? 'EN' : '中文';
  const langBtnLogin = document.getElementById('langBtnLogin');
  if (langBtnLogin) langBtnLogin.textContent = _lang === 'zh' ? 'EN' : '中文';
}
